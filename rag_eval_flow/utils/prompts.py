def format_judge_system_prompt(
    metric_name: str, 
    requirements: MetricRequirements
) -> str:
    """
    Generates a modular system prompt with bracketed, titlecase input names.
    """
    # Dynamically determine which inputs will be provided to the judge.
    inputs = []
    if requirements.query_required:
        inputs.append("[Query]")
    if requirements.context_required:
        inputs.append("[Context]")
    if requirements.ground_truth_required:
        inputs.append("[Ground Truth Answer]")
    
    # The SLM Response is always provided for evaluation.
    inputs.append("[SLM Response]")

    # Helper to create a grammatically correct list (e.g., "A, B, and C").
    if len(inputs) > 2:
        provided_inputs_str = f"{', '.join(inputs[:-1])}, and {inputs[-1]}"
    else:
        provided_inputs_str = " and ".join(inputs)

    # The main prompt template uses f-strings to insert the modular parts.
    return f"""
# Role and Objective
You are an expert evaluator assessing a generated response according to the **{metric_name.upper()}** metric.

## Metric Definition
{requirements.sys_definition}

# Evaluation Instructions
You will be provided with {provided_inputs_str}. Your task is to evaluate the **[SLM Response]** based on the criteria in the metric definition.

Be as objective as possible. Base your judgment strictly on the provided materials and metric definition. Do not let the response's tone or conclusions influence your evaluation.

Only output your evaluation, and make sure it is in the exact format specified below.

# Output Format
Output your evaluation in the following JSON format.
{{
    "explanation": "<your analysis>",
    "score": <your score from 0 to 5, or 0/1 if applicable>
}}
"""


def format_judge_user_prompt(
    requirements: MetricRequirements,
    response: str,
    **data: Dict[str, Any],
) -> str:
    """
    Formats the data payload for the judge based on the metric's requirements.

    This prompt contains ONLY the data for evaluation. All instructions
    are expected to be in the system prompt.
    """
    prompt_parts = []

    # Configuration for each potential data section
    # Maps the data key to its display header and requirement flag.
    section_config = {
        "query": ("[Query]", "query_required"),
        "context": ("[Context]", "context_required"),
        "ground_truth": ("[Ground Truth Answer]", "ground_truth_required"),
    }

    for key, (header, req_flag) in section_config.items():
        # Include a section only if the metric requires it AND the data is provided.
        if getattr(requirements, req_flag) and key in data and data[key]:
            content = data[key]
            # Special formatting for a list of context documents
            if key == "context" and isinstance(content, list):
                docs = [f"[Document {i+1}]\n{doc}" for i, doc in enumerate(content)]
                formatted_content = f"{header}\n" + "\n\n".join(docs)
            elif key == "context" and isinstance(content, list) and isinstance(content[0], Dict):
                docs = [f"[Document {i+1}]\n{doc['text']}" if "text" in doc.keys() else f"[Document {i+1}]\n{doc}" for i, doc in enumerate(content)]
                formatted_content = f"{header}\n" + "\n\n".join(docs)
            else:
                formatted_content = f"{header}\n{content}"
            prompt_parts.append(formatted_content)

    # The response being evaluated is always the final component.
    prompt_parts.append(f"[SLM Response]\n{response}")

    return "\n\n".join(prompt_parts)

